/*As interfaces da camada de aplicação em DDD definem contratos para os serviços que a camada de aplicação deve fornecer.
 * nelas, são especificados os métodos e operações que a camada de aplicação deve implementar para interagir com a camada de domínio 
 * e outras partes do sistema.
 * 
 * 
 * 
 * A camada de aplicação na arquitetura DDD é responsável por orquestrar as operações do sistema, 
 * gerenciando a comunicação entre a camada de domínio e as camadas externas, como a interface do usuário e a infraestrutura. 
 * Ela atua como um intermediário que coordena as atividades do domínio, 
 * garantindo que as regras de negócio sejam aplicadas corretamente e que os dados sejam processados de acordo com os requisitos do sistema.
 * 
 */
